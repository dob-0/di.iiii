import { mkdir, writeFile } from 'node:fs/promises'
import path from 'node:path'
import { parseArgs } from 'node:util'

const { values } = parseArgs({
    options: {
        output: {
            type: 'string'
        }
    }
})

const outputPath = path.resolve(process.cwd(), values.output || '.deploy/cpanel/serverXR/.env.generated')

const formatEnvValue = (value) => {
    const text = String(value ?? '')
    if (/^[A-Za-z0-9_./,:@+-]*$/.test(text)) {
        return text
    }
    return JSON.stringify(text)
}

const SAFE_SERVER_API_TOKEN_PATTERN = /^[A-Za-z0-9._~+/\-=]{16,}$/

const normalizeServerApiToken = (value = '') => String(value || '').trim().replace(/^bearer\s+/i, '')

const requiredValues = {
    NODE_ENV: process.env.NODE_ENV || 'production',
    PORT: process.env.PORT || '',
    APP_BASE_PATH: process.env.APP_BASE_PATH || '/serverXR',
    DATA_ROOT: process.env.DATA_ROOT || '',
    API_TOKEN: process.env.API_TOKEN || '',
    REQUIRE_AUTH: process.env.REQUIRE_AUTH || 'true',
    CORS_ORIGINS: process.env.CORS_ORIGINS || '',
    MAX_UPLOAD_MB: process.env.MAX_UPLOAD_MB || '100'
}

const missingEntries = Object.entries(requiredValues)
    .filter(([, value]) => !String(value || '').trim())
    .map(([key]) => key)

if (missingEntries.length) {
    throw new Error(`Missing required environment values: ${missingEntries.join(', ')}`)
}

requiredValues.API_TOKEN = normalizeServerApiToken(requiredValues.API_TOKEN)

if (!SAFE_SERVER_API_TOKEN_PATTERN.test(requiredValues.API_TOKEN)) {
    throw new Error(
        'Malformed API_TOKEN value. Expected a single token value without whitespace or shell commands.'
    )
}

const optionalValues = {
    SHARED_ROOT: process.env.SHARED_ROOT || '',
    SPACE_TTL_MS: process.env.SPACE_TTL_MS || '',
    SPACES_DIR: process.env.SPACES_DIR || '',
    UPLOADS_DIR: process.env.UPLOADS_DIR || ''
}

const envLines = [
    '# Generated by scripts/write-server-env.mjs',
    '# Do not commit this file with real secrets.',
    '',
    '# Node.js runtime',
    `NODE_ENV=${formatEnvValue(requiredValues.NODE_ENV)}`,
    `PORT=${formatEnvValue(requiredValues.PORT)}`,
    '',
    '# Public route mounted by the cPanel Node.js App',
    `APP_BASE_PATH=${formatEnvValue(requiredValues.APP_BASE_PATH)}`,
    '',
    '# Persisted server data',
    `DATA_ROOT=${formatEnvValue(requiredValues.DATA_ROOT)}`,
    '',
    '# Security',
    `API_TOKEN=${formatEnvValue(requiredValues.API_TOKEN)}`,
    `REQUIRE_AUTH=${formatEnvValue(requiredValues.REQUIRE_AUTH)}`,
    '',
    '# Allowed frontend origins',
    `CORS_ORIGINS=${formatEnvValue(requiredValues.CORS_ORIGINS)}`,
    '',
    '# Upload size limit in MB',
    `MAX_UPLOAD_MB=${formatEnvValue(requiredValues.MAX_UPLOAD_MB)}`
]

Object.entries(optionalValues).forEach(([key, value]) => {
    const normalizedValue = String(value || '').trim()
    if (!normalizedValue) {
        return
    }
    envLines.push(`${key}=${formatEnvValue(normalizedValue)}`)
})

await mkdir(path.dirname(outputPath), { recursive: true })
await writeFile(outputPath, `${envLines.join('\n')}\n`, 'utf8')

console.log(`[env:serverxr] Wrote ${outputPath}`)

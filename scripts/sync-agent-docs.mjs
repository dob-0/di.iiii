export const getGeneratedEntries = () => {
  const entries = []

  for (const scope of AI_DOC_SCOPES) {
    entries.push({
      path: scopeBridgePath(scope, 'CLAUDE.md'),
      content: renderClaudeBridge(scope)
    })
    entries.push({
      path: scopeBridgePath(scope, 'GEMINI.md'),
      content: renderGeminiBridge(scope)
    })
  }

  entries.push({
    path: '.github/copilot-instructions.md',
    content: renderCopilotRepoInstructions()
  })

  for (const scope of AI_DOC_SCOPES.filter((entry) => entry.applyTo)) {
    entries.push({
      path: normalizePath(path.join('.github/instructions', `${scope.slug}.instructions.md`)),
      content: renderCopilotScopeInstructions(scope)
    })
  }

  for (const scope of AI_DOC_SCOPES) {
    entries.push({
      path: normalizePath(path.join('.cursor/rules', scope.cursorRuleFile)),
      content: renderCursorRule(scope)
    })
  }

  return entries.sort((a, b) => a.path.localeCompare(b.path))
}

const ensureTrailingNewline = (value) => value.endsWith('\n') ? value : `${value}\n`

const writeFileIfChanged = async (relativePath, content) => {
  const absolutePath = toAbsolute(relativePath)
  await fs.mkdir(path.dirname(absolutePath), { recursive: true })
  const nextContent = ensureTrailingNewline(content)

  let currentContent = null
  try {
    currentContent = await fs.readFile(absolutePath, 'utf8')
  } catch (error) {
    if (error.code !== 'ENOENT') throw error
  }

  if (currentContent === nextContent) {
    return { relativePath, changed: false }
  }

  await fs.writeFile(absolutePath, nextContent)
  return { relativePath, changed: true }
}

export const syncGeneratedAgentDocs = async () => {
  const results = []
  for (const entry of getGeneratedEntries()) {
    results.push(await writeFileIfChanged(entry.path, entry.content))
  }
  return results
}

const isMain = path.resolve(process.argv[1] || '') === __filename

if (isMain) {
  const results = await syncGeneratedAgentDocs()
  const changed = results.filter((entry) => entry.changed)

  if (!changed.length) {
    console.log('AI doc bridges are already up to date.')
  } else {
    console.log('Updated AI doc bridges:')
    changed.forEach((entry) => {
      console.log(`- ${entry.relativePath}`)
    })
  }
}

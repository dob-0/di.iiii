const generatedHeader = (source) => `<!-- AUTO-GENERATED by \`npm run docs:ai:sync\` from ${source}. Do not edit by hand. -->`

const normalizePath = (value) => value.split(path.sep).join('/')

const toAbsolute = (relativePath) => path.join(repoRoot, relativePath)

const relativePathFrom = (fromFile, toFile) => {
  const fromDir = path.dirname(fromFile)
  const target = path.relative(fromDir, toFile)
  return normalizePath(target || '.')
}

const scopeAgentsPath = (scope) => scope.dir === '.' ? 'AGENTS.md' : normalizePath(path.join(scope.dir, 'AGENTS.md'))

const scopeBridgePath = (scope, filename) => scope.dir === '.'
  ? filename
  : normalizePath(path.join(scope.dir, filename))

const scopeAgentsLinkFromSelf = 'AGENTS.md'

const docsIndexFrom = (relativeFile) => relativePathFrom(relativeFile, 'docs/ai/index.md')

const linkFrom = (relativeFile, targetFile) => relativePathFrom(relativeFile, targetFile)

